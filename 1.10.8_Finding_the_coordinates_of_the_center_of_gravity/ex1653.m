syms x y r ya1 ya2;
f=x^2+y^2-r^2

% При вращении полуокржности вокруг OX получим сферу
S1=4*pi*r^2
% Согласно первой теореме Гульдена
S2=(pi*r)*(2*pi*ya1)

eq=S1-S2
slv1=solve(eq,ya1)
% ya1=(2*r)/pi
% xa1=0

% При вращении полукруга вокруг OX получим шар
V1=4*pi*r^3/3
% Согласно второй теореме Гульдена
V2=(pi*r^2/2)*(2*pi*ya2)

eq=V1-V2
slv2=solve(eq,ya2)
% ya2=(4*r)/(3*pi)
% xa2=0

