% Euler method
% x'=(y-x)/t
% y'=(x+y)/t
% x(1)=1
% y(1)=1
% 1<=t<=2
% h=0.2
clear
digits(5)
syms t x y
f1(t,x,y)=(y-x)/t
f2(t,x,y)=(x+y)/t
T(1)=1;
h=0.2;
T=T(1):h:2
%    1.0000    1.2000    1.4000    1.6000    1.8000    2.0000
X(1)=1;
Y(1)=1;
for i=1:5
    X(i+1)=X(i)+h*f1(T(i),X(i),Y(i));
    Y(i+1)=Y(i)+h*f2(T(i),X(i),Y(i));
end
X
