% Runge-Kutta method
% y'=y-2*x/y
% y(0)=1
% [0;1]
% h=0.2
clear
digits(4)
syms x y
f(x,y)=y-2*x/y
X(1)=0;
h=0.2;
X=X(1):h:1
%         0    0.2000    0.4000    0.6000    0.8000    1.0000
Y(1)=1;
for i=1:5
    K=zeros(1,4);
    K(1)=vpa(h*f(X(i),Y(i)));
    K(2)=vpa(h*f(X(i)+h/2,Y(i)+K(1)/2));
    K(3)=vpa(h*f(X(i)+h/2,Y(i)+K(2)/2));
    K(4)=vpa(h*f(X(i)+h,Y(i)+K(3)));
    D_y=vpa(1/6*(K(1)+2*K(2)+2*K(3)+K(4)));
    Y(i+1)=Y(i)+D_y;
end
Y
