% Euler method
% y'=(x+y)*(1-x*y)/(x+2*y)
% y(0)=1
clear
digits(5)
syms x y
f(x,y)=(x+y)*(1-x*y)/(x+2*y)
X(1)=0;
h=0.2;
X=X(1):h:1
%         0    0.2000    0.4000    0.6000    0.8000    1.0000 
Y(1)=1;
for i=1:5
    Y(i+1)=Y(i)+h*f(X(i),Y(i));
end
